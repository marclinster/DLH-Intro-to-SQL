CREATE OR REPLACE VIEW mycustomer_with_purchase_view AS 
	SELECT 
		c.id AS customer_id,
		FORMAT ('%s %s', c.first_name, c.last_name) AS customer_name,
		c.since AS customer_since,
		o.id AS order_number,
		o.date AS order_date,
		p.name AS product_name,
		p.price AS product_unit_price,
		p.price * o.qty AS extended_price
	FROM mycustomer c
		LEFT JOIN myorder o ON c.id = o.customer_id
		LEFT JOIN myproduct p ON o.product_nbr = p.nbr;	

SELECT * FROM mycustomer_with_purchase_view;

