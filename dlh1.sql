    format_type(c.castsource, NULL) AS source_type,
    format_type(c.casttarget, NULL) AS target_type
FROM pg_cast c
JOIN pg_type s ON c.castsource = s.oid
JOIN pg_type t ON c.casttarget = t.oid
ORDER BY 1, 2;


	
